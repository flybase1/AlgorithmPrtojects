package LinkedList.LinkedList.SingleLinkedLlist;

// 链表
public class ListNode {
    int value;
    ListNode next;

    public ListNode(int value, ListNode next) {
        this.value = value;
        this.next = next;
    }

    public ListNode() {
    }

    public ListNode(int value) {
        this.value = value;
    }

    public ListNode(ListNode next) {
        this.next = next;
    }


}
